class Default_values
{
	long l;
	double d;
	String s;
	char ch;
	float f;
	int i;
	short sr;
	boolean bool;
	public static void main(String args[])
	{
		Default_values d1=new Default_values();
		System.out.println("Long default value is "+d1.l);
		System.out.println("Double default value is "+d1.d);
		System.out.println("String default value is "+d1.s);
		System.out.println("Character default value is "+d1.ch+"anu");
		System.out.println("Float default value is "+d1.f);
		System.out.println("Integer default value is "+d1.i);
		System.out.println("Short default value is "+d1.sr);
		System.out.println("Boolean default value is "+d1.bool);
	}
}